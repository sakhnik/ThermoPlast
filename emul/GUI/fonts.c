/*********************************************************************
 * Функція повертає довжину символьного рядка у пікселях             *
 *********************************************************************/
unsigned int GetStringLength(const unsigned char *font, const unsigned char *s)
{
	len=0;
	if (font==font8x8 || font==font8x16 || font==font8x8lite)
	{
		while (*s++)
			len+=8;
		return len;
	}
	return 0;
}

unsigned char *DrawAddr;
const unsigned char *DrawFontSrc;
unsigned char draw_bit, draw_base, draw_mask, draw_j;
/********************************************************************
 * Функція малює символьний рядок.                                  *
 * Параметри:                                                       *
 *   x, y -- невід"ємні координати лівого верхнього кута прямокут-  *
 *           ника, у який треба намалювати рядок                    *
 *   str  -- вказівник на сам рядок                                 *
 *   font --  шрифт, який треба використати                         *
 * для прискорення роботи всі тіла BitCpy з необхідними змінами     *
 * перенесені всередину функції                                     *
 ********************************************************************/
void DrawString(unsigned int x, unsigned int y, 
				const unsigned char *str, const unsigned char *font)
{
//	if (x&3)
//		return;
//	x&=~3;

	if ((x>=SCREEN_WIDTH-8) || (y>=SCREEN_HEIGHT-8))
		return;
	len=x;
	if (font==font8x8)
	{
		while (*str)
		{
			if (*str=='\n')
			{
				y+=8;
				x=len;
				++str;
			}
			else
			{
//				BitCpy(x,y,8,8,font8x8 + ((*(str++))<<3));
				DrawFontSrc=font8x8 + ((*(str++))<<3);
				DrawAddr=VideoRam + ADDRESS(x,y);
				draw_bit=(y<SCREEN_HALF) ? 0 : 4;
				draw_base=SCREEN_QUATERNIONS - 2;//(SrcWidth >> 2);

				draw_mask = (y<SCREEN_HALF) ? 0xf0 : 0x0f;

				for (draw_j=0;draw_j<8;draw_j++)
				{
					*DrawAddr++ = (((*DrawAddr) & draw_mask) | (((*DrawFontSrc) >> 4) << draw_bit));
					*DrawAddr++ = (((*DrawAddr) & draw_mask) | (((*DrawFontSrc) &0x0f) << draw_bit));
					DrawFontSrc++;

					DrawAddr += draw_base; // наступний рядок
					if (DrawAddr >= VideoRam + SCREEN_TOTAL)
					{
				// Досягнуто нижньої половини екрана
						DrawAddr-=SCREEN_TOTAL;
						draw_bit+=4;
						draw_mask = draw_mask >> 4;
					}
				}
				x+=8;
			}
		}
		return;
	} 
	else if (font==font8x16)
	{
		while (*str)
		{
			if (*str=='\n')
			{
				y+=16;
				x=len;
				++str;
			}
			else
			{
//				BitCpy(x,y,8,16,font8x16 + ((*(str++))<<4));
				DrawFontSrc=font8x16 + ((*(str++))<<4);
				DrawAddr=VideoRam + ADDRESS(x,y);
				draw_bit=(y<SCREEN_HALF) ? 0 : 4;
				draw_base=SCREEN_QUATERNIONS - 2;//(SrcWidth >> 2);

				draw_mask = (y<SCREEN_HALF) ? 0xf0 : 0x0f;

				for (draw_j=0;draw_j<16;draw_j++)
				{
					*DrawAddr++ = (((*DrawAddr) & draw_mask) | (((*DrawFontSrc) >> 4) << draw_bit));
					*DrawAddr++ = (((*DrawAddr) & draw_mask) | (((*DrawFontSrc) &0x0f) << draw_bit));
					DrawFontSrc++;

					DrawAddr += draw_base; // наступний рядок
					if (DrawAddr >= VideoRam + SCREEN_TOTAL)
					{
				// Досягнуто нижньої половини екрана
						DrawAddr-=SCREEN_TOTAL;
						draw_bit+=4;
						draw_mask = draw_mask >> 4;
					}
				}
				x+=8;
			}
		}
		return;
	}
	else if (font==font8x8lite)
	{
		while (*str)
		{
			if (*str=='\n')
			{
				y+=8;
				x=len;
				++str;
			}
			else
			{
//				BitCpy(x,y,8,8,font8x8lite + ((*(str++))<<3));
				DrawFontSrc=font8x8lite + ((*(str++))<<3);
				DrawAddr=VideoRam + ADDRESS(x,y);
				draw_bit=(y<SCREEN_HALF) ? 0 : 4;
				draw_base=SCREEN_QUATERNIONS - 2;//(SrcWidth >> 2);

				draw_mask = (y<SCREEN_HALF) ? 0xf0 : 0x0f;

				for (draw_j=0;draw_j<8;draw_j++)
				{
					*DrawAddr++ = (((*DrawAddr) & draw_mask) | (((*DrawFontSrc) >> 4) << draw_bit));
					*DrawAddr++ = (((*DrawAddr) & draw_mask) | (((*DrawFontSrc) &0x0f) << draw_bit));
					DrawFontSrc++;

					DrawAddr += draw_base; // наступний рядок
					if (DrawAddr >= VideoRam + SCREEN_TOTAL)
					{
				// Досягнуто нижньої половини екрана
						DrawAddr-=SCREEN_TOTAL;
						draw_bit+=4;
						draw_mask = draw_mask >> 4;
					}
				}
				x+=8;
			}
		}
		return;
	} 

}

